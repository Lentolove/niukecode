    public int majorityElement(int[] nums) {
        if (nums.length == 1) return nums[0];
        int hp = 0, num = 0;
        for (int a : nums) {
            if (hp == 0) {
                num = a;
                hp = 1;
            } else if (a != num) {
                hp--;
            } else {
                hp++;
            }
        }
        return num;
    }
